export const searchBrands = (query: string, type?: string) => {
  const brands = type ? getBrandsByType(type) : Object.keys(vehicleBrands);
  return brands.filter(brand => 
    brand.toLowerCase().includes(query.toLowerCase())
  );
};

export const getBrandLogo = (brandName: string): string | null => {
  // Handle case variations and find the brand
  const normalizedBrandName = brandName.trim();
  
  // Direct match
  if (vehicleBrands[normalizedBrandName as keyof typeof vehicleBrands]) {
    return vehicleBrands[normalizedBrandName as keyof typeof vehicleBrands].logo;
  }
  
  // Case-insensitive search
  const foundBrand = Object.keys(vehicleBrands).find(
    brand => brand.toLowerCase() === normalizedBrandName.toLowerCase()
  );
  
  if (foundBrand) {
    return vehicleBrands[foundBrand as keyof typeof vehicleBrands].logo;
  }
  
  // Handle common variations and aliases
  const brandAliases: { [key: string]: string } = {
    'massey ferguson': 'Massey Ferguson',
    'massey-ferguson': 'Massey Ferguson',
    'mf': 'Massey Ferguson',
    'new boy': 'New Boy',
    'newboy': 'New Boy',
    'fiat om': 'Fiat OM',
    'fiatom': 'Fiat OM',
    'w.heinemann': 'W.Heinemann',
    'w heinemann': 'W.Heinemann',
    'heinemann': 'W.Heinemann',
    'vdl bus': 'VDL',
    'vdl coach': 'VDL',
    'man': 'Man Truck',
    'john-deere': 'John Deere',
    'johndeere': 'John Deere',
    'new-holland': 'New Holland',
    'newholland': 'New Holland'
  };
  
  const aliasKey = normalizedBrandName.toLowerCase();
  if (brandAliases[aliasKey]) {
    const aliasedBrand = brandAliases[aliasKey];
    if (vehicleBrands[aliasedBrand as keyof typeof vehicleBrands]) {
      return vehicleBrands[aliasedBrand as keyof typeof vehicleBrands].logo;
    }
  }
  
  return null;
};
